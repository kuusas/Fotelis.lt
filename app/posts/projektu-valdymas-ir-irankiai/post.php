<p>Kasdien, vienaip ar kitaip, susiduriu su projektų valdymu. Kartais net tenka projekto vadovą valdyt, kad viskas vyktų nuosekliai ir tvarkingai. O tvarkos reikia, kad būtų rezultatai. Nors ir atrodo, kad viskas yra tiesiog obvious, pasirodo net ir projektų vadovai su patirtim to nesuvokia iki galo.</p>

<h2>Naudosiama terminija</h2>

<p>Komanda - jei dirbi prie projekto ne vienas - dirbi su komanda, bet kalbant apie projektus, kalbu apie komandas sudarytas 1+ žmonių, bet visa tai tikrai negalioja large-scale projektams.<br />
Projekto vadovas (PM) - projekto vadovas, su developmentu artimai draugaujantis žmogus, išmanantis savo darbą. Jeigu dirbote web-kepyklėlėj, tai ten sutikot personažų, su marketingo diplomu, tuo pačiu vardu - "projekto vadovas". Tas pseudo-specialistas turbūt paliko didelę profesinę traumą, tad iškarto įspėju, kad mano tekste bus vartojamas šis terminas, bet tai nė kiek nesusiję su jūsų nuoskaudom. Bandykit priimti šį terminą, kaip profesionalo apibūdinima.</p>

<h2>Projektai</h2>

<p>Su nedideliais projektais viskas gan paprasta. Išsidėstai funkcionalumą A4 lape, kartais ir pusės tokio užtenka, pasidarai sau task'us, eini, darai, baigta. O dabar pasitelkim fantaziją ir įsivaizduokim, kad prie projekto turi dirbti kolega dizaineris? Iškarto kyla klausimas - o kaip viską sutarsim su juo? Kaip jis žinos ką aš programuosiu, o kaip aš žinosiu ką jis paišys? Juolab, jeigu kiekvienas dirba iš namų. Ir išvis - kaip klientui duoti tai, ko jis tikisi? Progresą visom šalim irgi reikia kažkaip pateikti. Čia jau pradedam ieškoti kažkokių įrankių. Galbūt tokia problema išsisprendžia tiesiog su email ir skype. Bet jeigu darbas vyksta komandoj? Jeigu komanda sudaryta ne vien iš developerių ir dizainerių, o prisideda daugiau skirtingų rolių? Kaip visada visiems žinoti ką daryti, kaip gali kiekvienas būti naudingiausias duotoju momentu ir apskritai - žinoti visada, kas vyksta su projektu? Kiekvieną kartą klausti PM'o? Laiko gaišinimas, tiek tavo paties, tiek PM'o.<br />
Taupant savo ir kitų laiką informaciją visada, visada reikia struktūrizuoti. Reikalui esant, bet kuris komandoje esantis žmogus, privalo turėti galimybę rasti, tai ko jam reikia, negaišindamas kitų laiko. Ir įsivaizduokim, kad informacija yra išmėtyta, arba guli tik PM'o galvoj - augant komandai PM'o fulltime darbu patampa tiesiog atsakinėjimas į klausimus, pasikartojančius klausimus, plūstančius iš visų šalių. Efektyvu? Tikrai ne. Dar nepaminėjau atvejo, jeigu PM'as dirba vienoje laiko juostoje, o dalis komandos kitoje. Tokiu atveju PM'as net fiziškai nesugebėtų efektyviai atsakyti į visus klausimus. Jeigu iškilo klausimas - atsakymo reikia čia ir dabar, nes kitaip kenčia darbo efektyvumas. Grįžtant prie žemiskesnių palyginimų, be laiko juostų. Tarkim dirbam su freelanceriais - vieni dirba naktį, kiti anksti ryte, treti - neprognozuojamai. Tokia komanda kažin ar dažnai susitiks vienu metu online.<br />
Kaip viską daryti efektyviai?</p>

