<h2>Planavimas, progresas - projektų valdymo sistema?</h2>

<p>Šiem darbam atlikt yra daug galimybių. Galima sakyti skonio reikalas. Vieni viską deda į google docs ar spreadsheets. Kiti naudoja tam skirtas sistemas kaip Basecamp, ActiveCollab, treti - apsiriboja tiesiog todo listų appsais - Wunderlist, Google Tasks + Google Calendar.<br />
Pačiam niekada neteko naudoti patogaus toolso. Gal mano norai dideli, gal pats rinkdamasis ne visada žinau ko noriu. Patogiausias kiek teko naudoti - Basecamp. Būtent projekto milestone'ų dėliojimui, taskų kūrimui ir roadmap'o stebėjimui.<br />
